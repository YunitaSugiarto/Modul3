
package modul3;

public class TgsSekolah {

    public int Normatif;
    public int Adaptif;
    public int Produktif;

    public TgsSekolah(int Normatif, int Adaptif, int Produktif) {
        this.Normatif = Normatif;
        this.Adaptif = Adaptif;
        this.Produktif = Produktif;
    }

    public void setNormatif(int Normatif) {
        this.Normatif = Normatif;

    }

    public void setAdaptif(int Adaptif) {
        this.Adaptif = Adaptif;

    }

    public void setProduktif(int Produktif) {
        this.Produktif = Produktif;

    }

}
